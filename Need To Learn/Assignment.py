#operator
# =, +=, -=, *=, /=, %=, **=, //= **=, &=, |=, ^=, ~=, <<=, >>=
# = is are Assignment operator # a=5 # same as a=5
# += is are Addition operator # a+=5 # same as a=a+5
# -= is are Subtraction operator # a-=5 # same as a=a-5
# *= is are Multiplication operator # a*=5 # same as a=a*5
# /= is are Division operator # a/=5 # same as a=a/5
# %= is are Modulus operator # a%=5 # same as a=a%5
# **= is are Exponent operator # a**=5 # same as a=a**5
# //= is are Floor Division operator # a//=5 # same as a=a//5
# &= is are Bitwise AND operator # a&=5 # same as a=a&5
# |= is are Bitwise OR operator # a|=5 # same as a=a|5
# ^= is are Bitwise XOR operator # a^=5 # same as a=a^5
# <<= is are Bitwise right shift operator # a<<=5 # same as a=a<<5
# >>= is are Bitwise left shift operator # a>>=5 # same as a=a>>5


